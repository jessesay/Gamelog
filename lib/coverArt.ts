export function makeSteamLibraryCoverUrl(appId: number) {
  return `https://cdn.cloudflare.steamstatic.com/steam/apps/${appId}/library_600x900.jpg`;
}

export function getKnownCoverUrl(game: Pick<Game, "slug" | "title">) {
  const slug = game.slug || game.title.toLowerCase().trim().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
  const appId = KNOWN_STEAM_APP_IDS[slug];
  return appId ? makeSteamLibraryCoverUrl(appId) : null;
}

export function getEffectiveCoverUrl(game: Pick<Game, "cover_url" | "slug" | "title">) {
  return game.cover_url || getKnownCoverUrl(game) || null;
}

export function withKnownCover<T extends Partial<Game>>(game: T): T {
  if (game.cover_url || !game.title) return game;
  return { ...game, cover_url: getKnownCoverUrl({ title: game.title, slug: game.slug }) };
}
